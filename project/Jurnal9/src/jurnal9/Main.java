    private void update_bukuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_bukuActionPerformed
       int row=tabel_buku.getSelectedRow();
       if(row!=-1){
       if(kode_buku.getText().equalsIgnoreCase("")){
           
       }else{
           list_buku.setValueAt(kode_buku.getText(), row,0);
       }
       
       
        if(judul_buku.getText().equalsIgnoreCase("")){
           
       }else{
           list_buku.setValueAt(judul_buku.getText(), row,1);
       }
        
          if(tgl_terbit.getText().equalsIgnoreCase("")){
           
       }else{
           list_buku.setValueAt(tgl_terbit.getText(), row,2);
       }
          
         if(penggarang.getText().equalsIgnoreCase("")){
           
       }else{
           list_buku.setValueAt(penggarang.getText(), row,3);
       }
        
       
          tabel_buku.setModel(list_buku);
       }else{
           JOptionPane.showMessageDialog(null,"tidak ada baris yang diplih");
       }
    }//GEN-LAST:event_update_bukuActionPerformed

