    private void input_pinjamActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_input_pinjamActionPerformed
       Object cari_buku = null;
     
        for(int i=0; i<list_mahasiswa.getRowCount(); i++){
          
           if(list_buku.getValueAt(i, 0).equals(kode_buku_pinjam.getText())){
               cari_buku=list_buku.getValueAt(i, 1);
          break;
           }
       }
        if(cari_buku==null){
            JOptionPane.showMessageDialog(null,"kode buku tidak valid ");
            return;
        }
        
        Object cari_mhs = null;
        for(int i=0; i<list_mahasiswa.getRowCount(); i++){
            System.out.println(list_mahasiswa.getValueAt(i, 0));
           if(list_mahasiswa.getValueAt(i, 0).equals(nim_pinjam.getText())){
               cari_mhs=list_mahasiswa.getValueAt(i,1 );
                      break;
           }
       }
        if(cari_mhs==null){
            JOptionPane.showMessageDialog(null,"nim tidak valid ");
           return;
        }
        
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/M/yyyy");
            Date tgl = sdf.parse(tgl_pinjam.getText());
            
            String date_pinjam = sdf.format(tgl);
            tgl.setDate(tgl.getDate()+6);
            String  date_balik =sdf.format(tgl);
            list_pinjam.addRow(new Object[]{kode_pinjam.getText(),kode_buku_pinjam.getText(),cari_buku,nim_pinjam.getText(),cari_mhs,date_pinjam,date_balik});
            
     
        } catch (ParseException ex) {
          JOptionPane.showMessageDialog(null,"tanngal slah format");
        }
       
    }//GEN-LAST:event_input_pinjamActionPerformed

